import streamlit as st
import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt
import statsmodels.formula.api as smf

st.set_page_config(page_title="ACDT Group 31 ‚Äì GERD & GDP Analysis", layout="wide")

@st.cache_data
def load_data():
    try:
        # Step 1Ô∏è‚É£ Load file with no header
        df_raw = pd.read_csv("ACDT_final_dataset.csv", header=None, encoding="utf-8-sig")

        # Step 2Ô∏è‚É£ Handle potential multi-type header row
        header_candidate = df_raw.iloc[1]

        # üí° Fix: if header row is a DataFrame (1-column CSV collapse), flatten it
        if isinstance(header_candidate, pd.DataFrame):
            header_candidate = header_candidate.iloc[0]

        # üí° Force conversion to string for header row
        header_row = pd.Series(header_candidate.astype(str).values)

        # Step 3Ô∏è‚É£ Drop first two metadata rows
        df = df_raw.iloc[2:].copy()
        df.columns = header_row

        # Step 4Ô∏è‚É£ Clean numeric columns
        for col in df.columns:
            df[col] = (
                df[col]
                .astype(str)
                .str.replace(",", "", regex=False)
                .str.replace(r"[^0-9eE+.-]", "", regex=True)
            )
            df[col] = pd.to_numeric(df[col], errors="coerce")

        # Step 5Ô∏è‚É£ Remove empty rows
        df.dropna(how="all", inplace=True)

        # Step 6Ô∏è‚É£ Rename main columns
        rename_map = {
            "Real GDP (billion USD, PPP-based, Chain-weighted, 2020 base year)": "Real_GDP",
            "Gross Domestic Expenditure on R&D (GERD) (current PPP USD)": "GERD",
        }
        df.rename(columns=rename_map, inplace=True)

        # Step 7Ô∏è‚É£ Log transformations
        df["ln_GDP"] = np.log(df["Real_GDP"].replace(0, np.nan))
        df["ln_GERD"] = np.log(df["GERD"].replace(0, np.nan))

        return df

    except Exception as e:
        st.error(f"‚ùå Error loading data: {e}")
        return pd.DataFrame()

# ================================================================
# üñ•Ô∏è Streamlit App Layout
# ================================================================
st.title("üìà GERD and GDP Full Regression Dashboard ‚Äì ACDT Group 31")

df = load_data()
if df.empty:
    st.error("‚ö†Ô∏è Dataset could not be loaded. Please verify structure or encoding.")
    st.stop()
else:
    st.success("‚úÖ Data successfully loaded and cleaned!")
    st.dataframe(df.head())

